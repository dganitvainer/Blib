package enums;

/**
 * Represents the type of a user in the library system. This enum defines all
 * possible user types that can login into the library management system.
 * 
 */
public enum UserType {
	/** Represents a user that has a subscription to the library */
	Subscriber,

	/**
	 * Represents a user that is a librarian in the library and uses the library
	 * management system
	 */
	Librarian
}
